    //Allows the user to add words to the set by directing to the addWordform
    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        //Opens the addword window
        AddWordForm aw = new AddWordForm(this, true, phoneticLanguage);
        aw.setVisible(true);
        
        //Increments the number of words and stores the word in the array
        //Mastery Factor: Arrays
        wordArray[numWords++] = aw.getWord();
        
        //Updates the list field
        //Mastery Factor: Additional Java Libraries
        WordSet.setListData(Arrays.copyOfRange(wordArray, 0, numWords));
    }//GEN-LAST:event_AddActionPerformed

    //Allows the user to edit words in the set
    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        //Prevents editing without selecting the word
        if (WordSet.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "You must select a word to edit");
        } else {
            //Opens the edit word form
            AddWordForm aw;
            Word selectedWord = wordArray[WordSet.getSelectedIndex()];
            if (phoneticLanguage) {
                aw = new AddWordForm(this, true, (WordWPron) selectedWord, phoneticLanguage);
            } else {
                aw = new AddWordForm(this, true, (WordWOPron) selectedWord, phoneticLanguage);
            }
            aw.setVisible(true);
            //Updates
            wordArray[WordSet.getSelectedIndex()] = aw.getWord();
            WordSet.setListData(Arrays.copyOfRange(wordArray, 0, numWords));
        }
    }//GEN-LAST:event_EditActionPerformed

    //Saves the vocabulary set
    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        //Stores the title of the vocabulary set
        title = Title.getText();
        if (title.equals("")) {
            //Prevents saving without a title
            JOptionPane.showMessageDialog(null, "Please indicate the title of the vocabulary set");
        } else if (title.length() > AppLogic.TITLE_RECORD_SIZE) {
            //Limits the number of characters that user can for the title
            JOptionPane.showMessageDialog(null, "Please limit the title of your vocabulary set to "
                    + AppLogic.TITLE_RECORD_SIZE + " characters");
        } else if (numWords == 0) {
            //Prevents saving if no words in the set
            JOptionPane.showMessageDialog(null, "You have not entered any words to the set");
        } else {
            title = Title.getText();
            //Closes the form
            this.dispose();
        }
    }//GEN-LAST:event_SaveActionPerformed

