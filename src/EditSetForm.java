    //Deletes the word in the set
    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        //Prevents deletion without specifying which word
        if (WordSet.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "You must select a word to delete");
        } else {
            if (WordSet.getSelectedIndex() + 1 == numWords) {
                //Deletes the last member of the word array
                wordArray[WordSet.getSelectedIndex()] = null;
            } else {
                //Rearranges the array
                for (int i = WordSet.getSelectedIndex(); i < numWords - 1; i++) {
                    wordArray[i] = wordArray[i + 1];
                }
                //Deletes the last member
                wordArray[numWords - 1] = null;
            }
            //Decrements number of words
            numWords--;
        }
        //Updates the list field
        WordSet.setListData(Arrays.copyOfRange(wordArray, 0, numWords));
    }//GEN-LAST:event_DeleteActionPerformed
